# 4. Find the Simple Interest on Rs. 200 for 5 years at 5% per year.

# Given values
P = 200  # Principal 
R = 5    # Rate of interest 
T = 5    # Years

# Calculate Simple Interest
SI = (P * R * T) / 100

# Display the result
print(f"The Simple Interest on Rs. {P} for {T} years at {R}% per year is: Rs. {SI}")